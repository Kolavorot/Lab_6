#В парламентскую комиссию нужно выбрать К членов.
#Претендентов предоставили N партий. Вывести все возможные варианты комиссии
#(от каждой партии должно быть от 1 до 3 членов).


import itertools
K = int(input('Введите колличество членов комиссии  '))
N = int(input('Введите колличество партий '))
if K < N:
    print("Ошибка ввода")
else:
    print(sorted(list(filter(lambda x: sum(x) == K, itertools.product(range(1, 4), repeat= N)))))
